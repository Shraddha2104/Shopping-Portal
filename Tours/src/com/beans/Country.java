package com.beans;

public class Country {

	private int id;
	private String name;
	private String flight_price;
	private String s2;
	private String s3;
	private String s5;
	private String continent_id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getFlight_price() {
		return flight_price;
	}

	public void setFlight_price(String flight_price) {
		this.flight_price = flight_price;
	}

	public String getS2() {
		return s2;
	}

	public void setS2(String s2) {
		this.s2 = s2;
	}

	public String getS3() {
		return s3;
	}

	public void setS3(String s3) {
		this.s3 = s3;
	}

	public String getS5() {
		return s5;
	}

	public void setS5(String s5) {
		this.s5 = s5;
	}

	public String getContinent_id() {
		return continent_id;
	}

	public void setContinent_id(String continent_id) {
		this.continent_id = continent_id;
	}

}
